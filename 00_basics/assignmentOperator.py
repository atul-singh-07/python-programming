# assigment operators
num =10

# num=num+10 ---> 20
num+=10
print("sum:",num)

# num=num-10 ----> 0
num=10
num-=10
print("sub:",num)

# num=num*10 ----> 100
num=10
num*=10
print("multiply:",num)

# num=num/10 ----> 1
num=10
num/=10
print("div:",num)

# num=num%10 ----> 0
num=10
num%=10
print("modulo:",num)

# num=num**10 ----> 10000000000
num=10
num**=10
print("power:",num)
